#include <iostream>
using namespace std;
int main()
{
    int num1, num2, add;
    //Taking input
    cout << "Enter first number: ";
    cin >> num1;
    cout << "Enter second number: ";
    cin>> num2;

    // adding the numbers stored in the variables
    add = num1 + num2;

    // Printing result
    cout << num1 << " + " << num2 << " = " << add;     
}
